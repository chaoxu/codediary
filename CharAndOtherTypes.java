
public class CharAndOtherTypes {
	public static void main(String[] args) {
		
		char a = (char)0XAB0041;
		System.out.println(a);
		
		char b = (char)65.25;
		System.out.println(b);
		
		int c = (int)'A';
		System.out.println(c);
		
		int d = (int)'1';
		System.out.println(d);
		
		char e = (char)0X40;
		System.out.println(e);
		
	}
}
