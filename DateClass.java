import java.util.*;

public class DateClass {
	public static void main(String[] args) {
		Date myDate1 = new Date(10000);
		System.out.println(myDate1.toString());
		
		Date myDate2 = new Date(100000);
		System.out.println(myDate2.toString());
		
		Date myDate3 = new Date(1000000);
		System.out.println(myDate3.toString());
		
		Date myDate4 = new Date(10000000);
		System.out.println(myDate4.toString());
		
		Date myDate5 = new Date(100000000);
		System.out.println(myDate5.toString());
		
		Date myDate6 = new Date(1000000000);
		System.out.println(myDate6.toString());

	}

}
